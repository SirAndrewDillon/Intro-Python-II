# Implement a class to hold room information. This should have name and
# description attributes.

# Encapsulation - Every object needs some sort of external facing API on order to manuipulate its internal state

# Inheritance - Allows us to leverage pre existing code that exists on a base case.
# Explicitly forms an object hierarchy

# Polymorphism

# In Python, you should use dictionaries to store key value pairs.


class Room:
    def __init__(self, name, description):
        self.name = name
        self.description = description
